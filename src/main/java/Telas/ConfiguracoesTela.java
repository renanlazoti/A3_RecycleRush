      private void pesquisarUsuario() {
		if (txtIdUsuario.getText().isEmpty()) {
			JOptionPane.showMessageDialog(null, "Digite o id da lixeira");
			txtIdUsuario.requestFocus();
		} else {
			String read = "select * from tb_usuario where id = ?";
			try {
				Connection con = cf.conectar();
				PreparedStatement pst = con.prepareStatement(read);
				pst.setString(1, txtIdUsuario.getText());
				ResultSet rs = pst.executeQuery();
				if (rs.next()) {				
                                        txtNomeExibicao.setText(rs.getString("nomeExibicao")); 
                                        txtLoginUsuario.setText(rs.getString("login"));
                                        txtSenhaUsuario.setText(rs.getString("senha"));
                                        cboPerfilUsuario.setSelectedItem(rs.getString("perfil"));
				} else {
					JOptionPane.showMessageDialog(null, "Usuario não cadastrado");                                                                            
				}
				con.close();
			} catch (Exception e) {
				System.out.println(e);
			}
		}
	}
      
      
      private void adicionarUsuario() {

                String Senha = new String(txtSenhaUsuario.getPassword());
        
		if (txtNomeExibicao.getText().isEmpty()) {
			JOptionPane.showMessageDialog(null, "Informe o nome de exibição!");
			txtNomeExibicao.requestFocus();
		}else if (txtLoginUsuario.getText().isEmpty()) {
			JOptionPane.showMessageDialog(null, "Informe o login do usuario");
			txtLoginUsuario.requestFocus();
		}else if (txtSenhaUsuario.getText().isEmpty()) {
			JOptionPane.showMessageDialog(null, "Informe a senha do usuario");
			txtSenhaUsuario.requestFocus();
                }else if (cboPerfilUsuario.getSelectedItem().equals("")) {
			JOptionPane.showMessageDialog(null, "Informe o perfil do usuario");
			cboPerfilUsuario.requestFocus();
		}else{
			String create = "insert into tb_usuario(nomeExibicao, login, senha, perfil) values (?,?,md5(?),?)";
			try {
				Connection con = cf.conectar();
				PreparedStatement pst = con.prepareStatement(create);
				pst.setString(1, txtNomeExibicao.getText());
				pst.setString(2, txtLoginUsuario.getText());
				pst.setString(3, Senha);
                                pst.setString(4, cboPerfilUsuario.getSelectedItem().toString());
				pst.executeUpdate();
				JOptionPane.showMessageDialog(null, "Usuario Cadastrado!");
                                this.dispose();
                                limparCampos();
                                txtIdUsuario.setText(null);                   
				con.close(); 
			}catch (SQLIntegrityConstraintViolationException ex) {
				JOptionPane.showMessageDialog(null, "Login em uso.\nEscolha outro login.");
				txtLoginUsuario.setText(null);
				txtLoginUsuario.requestFocus();
                        }catch (Exception e) {
				e.printStackTrace();
			}
		}
	}
      
