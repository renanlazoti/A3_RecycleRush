       private void excluirUsuario() {
        if (txtIdUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Id do usuário");
            txtIdUsuario.requestFocus();
        } else{
                int confirma = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do usuário?", "Atenção!", JOptionPane.YES_NO_OPTION);
                if (confirma == JOptionPane.YES_OPTION) {
                        String delete = "delete from tb_usuario where id=?";
                try {
                    Connection con = cf.conectar();
                    PreparedStatement pst = con.prepareStatement(delete);
                    pst.setString(1, txtIdUsuario.getText());
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Usuário excluído com sucesso");
                    limparCampos();
                    txtIdUsuario.setText(null);
                    con.close();
                } catch (Exception e) {
                    System.out.println(e);
               }
             }
          }
       }
        
